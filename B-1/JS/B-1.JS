$(function(){//문서를 로딩 후 실행하라
    
    // 📌 탭메뉴 클릭 이벤트
    $(".tabmenu li a").click(function (e) {
    e.preventDefault(); // a 태그의 기본 동작(# 이동) 막기

    // 1️⃣ 모든 탭(li)에서 .on 제거 → 비활성화
    $(".tabmenu li").removeClass("on");

    // 2️⃣ 지금 클릭한 a가 들어있는 li를 찾아서 .on 추가 → 활성화
    $(this).closest("li").addClass("on");

    // 3️⃣ 현재 클릭한 li가 몇 번째인지 순서(index)를 구함
    let index = $(this).closest("li").index();

    // 4️⃣ 모든 탭 내용(.tabcon)을 일단 숨김
    $(".tabcon").hide();

    // 5️⃣ 클릭한 순서(index)에 해당하는 탭 내용만 보이기
    $(".tabmenu li").eq(index).find(".tabcon").show();
    });


})

